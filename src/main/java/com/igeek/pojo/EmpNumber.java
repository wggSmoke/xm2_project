package com.igeek.pojo;

public class EmpNumber {
    private Integer depId;
    private  Integer depNumber;
    private  Double depSalary;

    public Integer getDepId() {
        return depId;
    }

    public void setDepId(Integer depId) {
        this.depId = depId;
    }

    public Integer getDepNumber() {
        return depNumber;
    }

    public void setDepNumber(Integer depNumber) {
        this.depNumber = depNumber;
    }

    public Double getDepSalary() {
        return depSalary;
    }

    public void setDepSalary(Double depSalary) {
        this.depSalary = depSalary;
    }
}
